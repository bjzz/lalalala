+ (CategoryNodesEnum)categoryNodesByMediaType:(MediaTypeEnum)mediatype {
    CategoryNodesEnum retCategoryNodes = Category_Music;
    switch (mediatype) {
        case Audio: {
            retCategoryNodes = Category_Music;
            break;
        }
        case Video: {
            retCategoryNodes = Category_Movies;
            break;
        }
        case TVAndMusic:
        case TVShow: {
            retCategoryNodes = Category_TVShow;
            break;
        }
        case MusicVideo: {
            retCategoryNodes = Category_MusicVideo;
            break;
        }
        case Podcast:
        case VideoPodcast: {
            retCategoryNodes = Category_PodCasts;
            break;
        }
        case iTunesU:
        case iTunesUVideo: {
            retCategoryNodes = Category_iTunesU;
